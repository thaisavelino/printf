int		ft_max(int *ptr, int size)
{
	int	i;
	int	max;

	if (size == 0)
		return (0);
	max = ptr[0];
	i = 1;
	while (i < size)
	{
		if (ptr[i] > max)
			max = ptr[i];
		i++;
	}
	return (max);
}
